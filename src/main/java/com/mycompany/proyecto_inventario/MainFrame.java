    private void button_loginMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_button_loginMouseClicked
        //Para entrar en la ventana de log in o indicar si ha puesto mal usuario/contraseña.
        if (this.usuario_correcto(textfield_user.getText(), this.textfield_password.getText())) {
            this.jTabbedPane1.setSelectedIndex(1);
        } else {
            JOptionPane.showMessageDialog(null, "¡Ha ingresado usuario o contraseña incorrecta!");
            this.inicializarLogIn();
        }
    }//GEN-LAST:event_button_loginMouseClicked

    private boolean usuario_correcto(String usuario_ingresado, String clave_ingresada){
        
        try (Connection conn = DriverManager.getConnection(funciones.getDB_URL(), funciones.getUSER(), funciones.getPASS()); CallableStatement stmt = conn.prepareCall("{call sp_usuario_read}")) {
            stmt.execute();
            ResultSet rs = stmt.getResultSet();
            ResultSetMetaData rsmd = rs.getMetaData();

            String usuario_unico;
            String clave;

            while (rs.next()) {
                usuario_unico = rs.getString(1);
                clave = rs.getString(2);
                
                // Vamos verificando cada usuario.
                if (usuario_unico.equals(usuario_ingresado) && clave.equals(clave_ingresada)){
                    return true;
                }
            }

        } catch (SQLException e) {
            System.out.println("No se ha conectado!");
        }
        
        return false;
    }
    
    
